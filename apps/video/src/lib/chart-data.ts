/** Indexed chart data — access by coin key (e.g. "xyz:TSLA", "BTC") */
export const CHART_DATA: Record<ChartAssetKey, OHLCPoint[]> = {
  "BTC": DATA_BTC,
  "ETH": DATA_ETH,
  "SOL": DATA_SOL,
  "xyz:TSLA": DATA_XYZ_TSLA,
  "xyz:NVDA": DATA_XYZ_NVDA,
  "xyz:GOLD": DATA_XYZ_GOLD,
  "xyz:SILVER": DATA_XYZ_SILVER,
  "xyz:AAPL": DATA_XYZ_AAPL,
};

/** Get the latest close price for an asset */
export function latestPrice(coin: ChartAssetKey): number {
  const data = CHART_DATA[coin];
  return data[data.length - 1].close;
}

/** Get the 24h price change % for an asset */
export function priceChange24h(coin: ChartAssetKey): { change: number; positive: boolean } {
  const data = CHART_DATA[coin];
  const now = data[data.length - 1].close;
  const h24 = data[Math.max(0, data.length - 25)].close;
  const change = ((now - h24) / h24) * 100;
  return { change: Math.round(change * 100) / 100, positive: change >= 0 };
}
